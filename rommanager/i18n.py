def tr(key: str, **kwargs) -> str:
    lang_map = _TRANSLATIONS.get(_current_language, _TRANSLATIONS[LANG_EN])
    text = lang_map.get(key, _TRANSLATIONS[LANG_EN].get(key, key))
    if kwargs:
        return text.format(**kwargs)
